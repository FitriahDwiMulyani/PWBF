        function nFormatter(num, digits) {
            const lookup = [
                { value: 1, symbol: "" },
                { value: 1e3, symbol: "K" },
                { value: 1e6, symbol: "M" },
                { value: 1e9, symbol: "G" },
                { value: 1e12, symbol: "T" },
                { value: 1e15, symbol: "P" },
                { value: 1e18, symbol: "E" }
            ];
            const rx = /\.0+$|(\.[0-9]*[1-9])0+$/;
            var item = lookup.slice().reverse().find(function(item) {
                return num >= item.value;
            });
            var splitNum = num.toString().split("")

            if(splitNum.length > 3) {
                splitNum[splitNum.length - 1] = 0
                splitNum[splitNum.length - 2] = 0
            }

            return item ? (splitNum.join('') / item.value).toFixed(digits).replace(rx, "$1") + `<span class="tw-text-xs tw-opacity-60 tw-ml-1">${item.symbol}</span>` : "0";
        }

